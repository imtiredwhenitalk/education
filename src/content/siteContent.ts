const uniqueStrings = (items: string[]): string[] => {
  const seen = new Set<string>();
  const result: string[] = [];

  items.forEach((item) => {
    const normalized = item.trim();
    if (!normalized || seen.has(normalized)) return;
    seen.add(normalized);
    result.push(normalized);
  });

  return result;
};

const enrichQuickInfoMap = (map: Record<string, QuickInfoItem>): Record<string, QuickInfoItem> => {
  const next: Record<string, QuickInfoItem> = {};

  Object.entries(map).forEach(([key, item]) => {
    const extra = expandedQuickInfoDetails[key] || [];
    next[key] = {
      ...item,
      details: uniqueStrings([...(item.details || []), ...extra]),
    };
  });

  return next;
};

