export const normalizeSiteContent = (incoming: Partial<SiteContent> | null | undefined): SiteContent => {
  if (!incoming || typeof incoming !== "object") {
    return defaultSiteContent;
  }

  const sourceMap =
    incoming.quickInfoMap && typeof incoming.quickInfoMap === "object"
      ? (incoming.quickInfoMap as Record<string, QuickInfoItem>)
      : defaultSiteContent.quickInfoMap;

  const map = enrichQuickInfoMap(sourceMap);

  const cards = Array.isArray(incoming.infoCards) && incoming.infoCards.length
    ? incoming.infoCards
    : defaultSiteContent.infoCards;

  const incomingLinks =
    incoming.distanceLearningLinks && typeof incoming.distanceLearningLinks === "object"
      ? (incoming.distanceLearningLinks as DistanceLearningLinks)
      : {};

  const distanceLearningLinks: DistanceLearningLinks = {
    ...defaultSiteContent.distanceLearningLinks,
    ...incomingLinks,
  };

  return {
    ...defaultSiteContent,
    ...incoming,
    quickInfoMap: map,
    distanceLearningLinks,
    infoCards: cards,
  };
};
